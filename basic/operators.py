a = 10
b = 3

# Arithmetic operators

print("Addition:", a + b)
print("Subtraction:", a - b)
print("Multiplication:", a * b)
print("Division:", a / b)
print("Modulus:", a % b)
print("Exponent:", a ** b)

# Comparison operators

print(a > b)
print(a < b)
print(a == b)
print(a != b)

# Logical operators

x = True
y = False

print(x and y)
print(x or y)
print(not x)
